/**
 * This program demonstrates the while loop.
 */

public class WhileLoop
{
   public static void main(String[] args)
   {
      int number = 0;

      while (number <1000)
      {
         System.out.printf("\nCurrent number: %d. Hello",number);
         number++;
      }

      System.out.println("\nThats all\n");
   }
}
